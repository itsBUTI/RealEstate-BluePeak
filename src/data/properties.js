function makeLocalGallery(seed, count = 3) {
  const pool = localImages.bco
  const start = Math.abs(Number(seed) || 0) % pool.length

  const safeCount = Math.max(1, Math.min(Number(count) || 3, pool.length))
  return Array.from({ length: safeCount }, (_, index) => pool[(start + index) % pool.length])
}

